/**
 * 获取城市电话区号
 * @param cityName 城市名称
 * @returns 电话区号,如果未找到返回"000"
 */
export function getCityAreaCode(cityName: string | null | undefined): string {
  if (!cityName) return "000";
  
  // 移除"市"、"省"等后缀
  const cleanCityName = cityName.replace(/[市省区县]/g, "");
  
  // 精确匹配
  if (CITY_AREA_CODES[cleanCityName]) {
    return CITY_AREA_CODES[cleanCityName];
  }
  
  // 模糊匹配(查找包含该城市名的键)
  for (const [city, code] of Object.entries(CITY_AREA_CODES)) {
    if (city.includes(cleanCityName) || cleanCityName.includes(city)) {
      return code;
    }
  }
  
  // 未找到返回默认值
  return "000";
}

/**
 * 生成订单号
 * @param cityName 城市名称
 * @returns 订单号格式: YYYYMMDDHHMMSS-区号-随机数
 */
export function generateOrderNo(cityName?: string | null): string {
  const now = new Date();
  const dateStr = now.getFullYear().toString() +
    (now.getMonth() + 1).toString().padStart(2, '0') +
    now.getDate().toString().padStart(2, '0') +
    now.getHours().toString().padStart(2, '0') +
    now.getMinutes().toString().padStart(2, '0') +
    now.getSeconds().toString().padStart(2, '0');
  
  const areaCode = getCityAreaCode(cityName);
  const randomSuffix = Math.random().toString(36).substring(2, 8).toUpperCase();
  
  return `${dateStr}-${areaCode}-${randomSuffix}`;
}
